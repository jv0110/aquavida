const open_modal = (content, func) => {
  const modal = document.querySelector('.modal-wrapper');
  const modal_content = document.querySelector('.modal-wrapper .content');
  const body = document.querySelector('html');

  modal_content.innerHTML = "";
  modal.classList.remove('hide');
  modal.classList.add('show');
  modal_content.innerHTML = content;
  body.style.overflow="hidden";
  func();
}
const close_modal = () => {
  const close_btn = document.querySelector('.modal-wrapper .btn');
  close_btn.addEventListener('click', e => {
    const modal = document.querySelector('.modal-wrapper');
    const body = document.querySelector('html');

    modal.classList.remove('show');
    modal.classList.add('hide');
    body.style.overflow="auto";
  });
}

const get_content_type = (type) => {
  return modal_content.filter(content => content.type === type);
}
const open_modal_click = () => {
  const items = [...document.querySelectorAll('img.item-modal'), ...document.querySelectorAll('.text.item-modal')];

  items.forEach(item => {
    item.addEventListener('click', e => {
      const clicked_el_type = item.getAttribute('data-type');
      if(clicked_el_type !== null){
        const type = get_content_type(clicked_el_type);
        open_modal(type[0].content, close_modal);
      }
    });
  })
}
open_modal_click();